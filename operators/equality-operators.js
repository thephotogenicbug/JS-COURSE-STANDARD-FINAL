// Strict Equality Operator 
// (Datatype + Value) should be same
console.log(1 === 1); // Number === Number, 1 is oponent of 1
console.log("---------------------------------")
console.log('1' === 1); // String === Number, 1 is oponent of 1
console.log("---------------------------------");


// Lose Equality Operator 
// In Loose Equality First value is important
console.log(1 == 1); // Number == Number
console.log("---------------------------------");
console.log('1' == 1); // String == Number 
console.log("---------------------------------");
console.log(true == '1'); // true == String
