/* comparison operator
    1 > 5
    Less <
    Great >
*/

let x = 5

// Relational Operators
console.log(x < 5); // Less than
console.log("---------------------");
console.log(x <= 5);  // LessThan or Equal too
console.log("---------------------")
console.log(x > 5);  // Greater than
console.log("---------------------");
console.log(x >= 5);  // GreaterThan or Equal too
console.log("--------------------------------------------------")


// Equality Operators
// (x === 5) operands
console.log(x === 5); // 5 === 5 Equal too
console.log("---------------------");
console.log(x !== 5); // 5 !== 5 Is not Equals too
console.log("--------------------------------------------------");

// String Comparison
console.log('Alen' < 'Naveen'); // dictionary mode
console.log("--------------------------------------------------");

// Comparison of different type
console.log('1' < 5); // 1 < 5
console.log("---------------------");
console.log(true == 0); // true == 1 which means true 
// Binary language
// 1 - True
// 0 - False



